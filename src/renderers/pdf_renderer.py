def render_tearsheet(ts, output_dir: Optional[str] = None) -> str:
    """
    Convenience wrapper: render a Tearsheet object to PDF and return the path.

    Parameters
    ----------
    ts          : Tearsheet (must be legal-ready)
    output_dir  : Optional override for the output directory path.

    Returns
    -------
    str  — absolute path of the generated PDF.
    """
    renderer = TearsheetPDFRenderer(
        output_dir=Path(output_dir) if output_dir else None
    )
    return renderer.render(ts)
