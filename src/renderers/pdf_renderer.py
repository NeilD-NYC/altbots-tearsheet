def _fmt_usd(value: Optional[int]) -> str:
    if value is None:
        return "—"
    if value >= 1_000_000_000:
        return f"${value / 1_000_000_000:.2f}B"
    if value >= 1_000_000:
        return f"${value / 1_000_000:.1f}M"
    if value >= 1_000:
        return f"${value / 1_000:.0f}K"
    return f"${value:,}"


def _recency_color(flag: str) -> colors.Color:
    return {"green": GREEN_FLAG, "yellow": AMBER_FLAG}.get(flag, RED_FLAG)


def _recency_label(flag: str, days: int) -> str:
    if days < 0:
        return "Unknown date"
    labels = {"green": "Current", "yellow": "Approaching stale", "red": "Stale"}
    return f"{labels.get(flag, 'Stale')} ({days}d ago)"


def _safe_str(val, fallback: str = "—") -> str:
    if val is None:
        return fallback
    s = str(val).strip()
    return s if s else fallback


def _truncate(text: str, max_chars: int) -> str:
    if len(text) <= max_chars:
        return text
    return text[: max_chars - 1] + "…"


def _gold_rule(width: float = CONTENT_W) -> HRFlowable:
    return HRFlowable(width=width, thickness=1, color=GOLD,
                      spaceAfter=4, spaceBefore=4)


def _navy_rule(width: float = CONTENT_W) -> HRFlowable:
    return HRFlowable(width=width, thickness=0.5, color=NAVY,
                      spaceAfter=3, spaceBefore=2)


