class TearsheetPDFRenderer:
    """
    Renders a Tearsheet object to a two-page PDF.

    Page 1: Header bar · Firm Overview · Key Personnel · SEC Filings Snapshot
    Page 2: Performance (confidence badge) · Red Flags · Social Signals
            [Gold rule] · Four-layer legal disclosure block (NEVER truncated)

    Usage
    -----
    renderer = TearsheetPDFRenderer()
    path = renderer.render(tearsheet)
    print(path)   # /root/tearsheet-project/output/Viking_Global_Investors.pdf
    """

    def __init__(self, output_dir: Optional[Path] = None):
        self.output_dir = Path(output_dir) if output_dir else OUTPUT_DIR
        self.output_dir.mkdir(parents=True, exist_ok=True)

    # ------------------------------------------------------------------
    # Public entry point
    # ------------------------------------------------------------------

    def render(self, ts) -> str:
        """
        Render the given Tearsheet to a PDF and return the output file path.

        Parameters
        ----------
        ts : Tearsheet
            Must satisfy ts.is_legal_ready (disclosure envelope attached).

        Returns
        -------
        str
            Absolute path of the generated PDF file.

        Raises
        ------
        ValueError
            If ts.disclosure is None (legal check fails).
        """
        if not ts.is_legal_ready:
            raise ValueError(
                "Tearsheet does not carry a DisclosureEnvelope. "
                "Run TearsheetAssembler.assemble() which attaches it as the final step."
            )

        styles = _build_styles()
        generated_date = self._format_date(ts.disclosure.generated_at)
        slug = self._safe_filename(ts.manager_name)
        out_path = str(self.output_dir / f"{slug}.pdf")

        decorator = _PageDecorator(ts.manager_name, generated_date, styles)

        # ── Document template ──────────────────────────────────────────────
        doc = BaseDocTemplate(
            out_path,
            pagesize=letter,
            leftMargin=MARGIN_H,
            rightMargin=MARGIN_H,
            topMargin=HEADER_H + MARGIN_TOP,
            bottomMargin=MARGIN_BOT,
            title=f"{ts.manager_name} — Institutional Tearsheet",
            author="AltBots",
            creator="AltBots Tearsheet Engine v1.0",
        )

        # Single-column frame for both pages
        frame = Frame(
            MARGIN_H, MARGIN_BOT,
            CONTENT_W, PAGE_H - HEADER_H - MARGIN_TOP - MARGIN_BOT,
            leftPadding=0, rightPadding=0,
            topPadding=0, bottomPadding=0,
            id="main",
        )
        template = PageTemplate(
            id="main_template",
            frames=[frame],
            onPage=decorator,
        )
        doc.addPageTemplates([template])

        # ── Build story ────────────────────────────────────────────────────
        story: list = []

        # ── PAGE 1 ─────────────────────────────────────────────────────────
        story += _build_firm_overview(ts, styles)
        story += _build_personnel_table(ts, styles)
        story += _build_sec_filings_snapshot(ts, styles)

        # ── PAGE 2 ─────────────────────────────────────────────────────────
        story.append(PageBreak())
        story += _build_performance_section(ts, styles)
        story += _build_red_flags_section(ts, styles)
        story += _build_social_signals_section(ts, styles)

        # Disclosure block — ALWAYS last; uses KeepTogether so it won't split.
        # Per spec: content compresses/overflows before this block is truncated.
        story += _build_disclosure_block(ts.disclosure, styles)

        doc.build(story)
        return out_path

    # ------------------------------------------------------------------
    # Utilities
    # ------------------------------------------------------------------

    @staticmethod
    def _format_date(iso_str: str) -> str:
        try:
            dt = datetime.fromisoformat(iso_str.replace("Z", "+00:00"))
            return dt.strftime("%B %d, %Y")
        except Exception:
            return str(date.today().strftime("%B %d, %Y"))

    @staticmethod
    def _safe_filename(name: str) -> str:
        safe = "".join(c if c.isalnum() or c in "-_ " else "_" for c in name)
        return safe.replace(" ", "_")[:80]


