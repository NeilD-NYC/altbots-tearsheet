def _build_disclosure_block(
    disclosure,               # DisclosureEnvelope
    styles: dict,
) -> list:
    """
    Returns a list of Platypus flowables for the mandatory four-layer
    legal disclosure block.  Preceded by a gold rule separator.
    The block is wrapped in KeepTogether so it will NOT be split across pages
    (it overflows onto a new page wholesale rather than be truncated).
    """
    S = styles

    block: list = []

    # Gold rule separator
    block.append(HRFlowable(
        width=CONTENT_W, thickness=1.5, color=GOLD,
        spaceAfter=DISC_TOP_PAD, spaceBefore=6,
    ))

    # Layer 1 — Not Investment Advice
    block.append(Paragraph(
        "<b>NOT INVESTMENT ADVICE.</b>  " + disclosure.not_investment_advice,
        S["legal"],
    ))

    # Layer 2 — Regulatory / ODD caveat
    block.append(Paragraph(
        "<b>REGULATORY SCREENING LIMITATION.</b>  " + disclosure.regulatory_disclaimer,
        S["legal"],
    ))

    # Layer 3 — Data limitation
    block.append(Paragraph(
        "<b>DATA LIMITATION.</b>  " + disclosure.data_limitation,
        S["legal"],
    ))

    # Layer 4 — Confidentiality
    block.append(Paragraph(
        "<b>CONFIDENTIALITY.</b>  " + disclosure.confidentiality,
        S["legal"],
    ))

    # Data sources line
    if disclosure.data_sources:
        src_txt = "Data sources consulted: " + "; ".join(disclosure.data_sources) + "."
        block.append(Paragraph(src_txt, S["legal"]))

    # Skipped modules
    if disclosure.modules_skipped:
        skipped = "; ".join(
            f"{mod} ({reason})"
            for mod, reason in disclosure.modules_skipped.items()
        )
        block.append(Paragraph(
            f"Modules unavailable during generation: {skipped}.",
            S["legal"],
        ))

    # Version / timestamp
    block.append(Paragraph(
        f"Tearsheet schema v{disclosure.version}  ·  Generated {disclosure.generated_at}",
        S["legal"],
    ))

    block.append(Spacer(1, DISC_BOTTOM_PAD))

    return [KeepTogether(block)]


