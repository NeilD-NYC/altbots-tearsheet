def _confidence_badge_para(flag: str, days: int, styles: dict) -> Paragraph:
    """Small inline confidence badge paragraph."""
    label = _recency_label(flag, days)
    col   = _recency_color(flag)
    hex_c = col.hexval() if hasattr(col, "hexval") else "#767676"
    txt = (
        f'<font color="{hex_c}"><b>▮ {label}</b></font>'
    )
    return Paragraph(txt, styles["body_sm"])


def _build_performance_section(ts, styles: dict) -> list:
    """
    Performance section — drawn from 13F data (AUM + holdings) with recency badge.
    The spec calls for a 'confidence badge'; we implement this as a recency badge
    since the tearsheet does not generate performance calculations.
    """
    S = styles
    items: list = []

    items.append(Paragraph("Performance & Portfolio Overview", S["h1"]))
    items.append(_navy_rule())

    edgar = ts.edgar
    if not edgar or not edgar.form_13f:
        items.append(Paragraph(
            "Portfolio performance data not available from public filings.",
            S["body_italic"],
        ))
        return items

    f13 = edgar.form_13f[0]

    # Confidence / recency badge
    if hasattr(f13, "recency") and f13.recency:
        flag = getattr(f13.recency, "flag", "red")
        days = getattr(f13.recency, "days_since_filing", -1)
    elif hasattr(f13, "source") and f13.source:
        filing_date = getattr(f13.source, "filing_date", None)
        if filing_date:
            try:
                from src.core.edgar import filing_recency_flag
                flag, days = filing_recency_flag(str(filing_date))
            except Exception:
                flag, days = "red", -1
        else:
            flag, days = "red", -1
    else:
        flag, days = "red", -1

    items.append(_confidence_badge_para(flag, days, S))
    items.append(Spacer(1, 4))

    rows = [
        ("Reported Portfolio Value",
         _fmt_usd(getattr(f13, "total_portfolio_value_usd", None))),
        ("Number of Positions",
         _safe_str(getattr(f13, "num_positions", None))),
        ("Top-10 Concentration",
         f"{f13.concentration_top10_pct:.1f}%"
         if getattr(f13, "concentration_top10_pct", None) is not None
         else "—"),
    ]

    if edgar.adv:
        adv = edgar.adv
        rows += [
            ("AUM (Total, Form ADV)",
             _fmt_usd(getattr(adv, "aum_total_usd", None))),
            ("AUM (Discretionary)",
             _fmt_usd(getattr(adv, "aum_discretionary_usd", None))),
            ("AUM (Non-Discretionary)",
             _fmt_usd(getattr(adv, "aum_non_discretionary_usd", None))),
        ]

    col_w = [1.8 * inch, CONTENT_W - 1.8 * inch]
    tdata = [[Paragraph(k, S["kv_key"]), Paragraph(v, S["kv_val"])]
             for k, v in rows]
    tbl = Table(tdata, colWidths=col_w)
    tbl.setStyle(TableStyle([
        ("ROWBACKGROUNDS", (0, 0), (-1, -1), [WHITE, GRAY_LIGHT]),
        ("TOPPADDING",    (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ("LEFTPADDING",   (0, 0), (-1, -1), 4),
        ("GRID",          (0, 0), (-1, -1), 0.25,
         colors.HexColor("#D0D0D0")),
    ]))
    items.append(tbl)

    items.append(Paragraph(
        "Portfolio values are as reported in the most recent Form 13F filing. "
        "Figures represent long positions held at quarter-end and may not reflect "
        "current holdings. This is not a performance record.",
        S["body_italic"],
    ))

    return items


def _build_red_flags_section(ts, styles: dict) -> list:
    S = styles
    items: list = []

    items.append(Spacer(1, 8))
    items.append(Paragraph("Regulatory Disclosures", S["h1"]))
    items.append(_navy_rule())

    rf = ts.red_flags
    if rf is None:
        items.append(Paragraph("Regulatory screening data unavailable.", S["body_italic"]))
        return items

    # Status line
    status_color = RED_FLAG if getattr(rf, "has_disclosures", False) else GREEN_FLAG
    hex_s = status_color.hexval() if hasattr(status_color, "hexval") else "#767676"
    items.append(Paragraph(
        f'<font color="{hex_s}"><b>{rf.status_phrase}</b></font>',
        S["body"],
    ))
    items.append(Spacer(1, 4))

    findings = getattr(rf, "findings", [])
    if not findings:
        items.append(Paragraph(
            "No public regulatory disclosures identified in Form ADV Item 11 "
            "or SEC enforcement records.",
            S["flag_body"],
        ))
    else:
        for idx, finding in enumerate(findings, 1):
            summary     = _safe_str(getattr(finding, "summary", None))
            source_lbl  = _safe_str(getattr(finding, "source_label", None))
            source_url  = _safe_str(getattr(finding, "source_url", None))
            filing_date = getattr(finding, "filing_date", None)
            item_ref    = getattr(finding, "item", None)

            header_parts = [f"({idx})"]
            if item_ref:
                header_parts.append(item_ref)
            if source_lbl and source_lbl != "—":
                header_parts.append(f"— {source_lbl}")
            items.append(Paragraph(" ".join(header_parts), S["h3"]))
            items.append(Paragraph(summary, S["flag_body"]))

            meta_parts = []
            if filing_date:
                meta_parts.append(f"Filing date: {filing_date}")
            if source_url and source_url != "—":
                meta_parts.append(f"Source: {source_url}")
            if meta_parts:
                items.append(Paragraph(" · ".join(meta_parts), S["url_sm"]))
            items.append(Spacer(1, 3))

    # Mandatory red-flag disclaimer
    items.append(Paragraph(
        getattr(rf, "disclaimer",
                "This screening is based on public records and is not exhaustive. "
                "It does not replace a comprehensive Operational Due Diligence review."),
        S["body_italic"],
    ))

    return items


def _build_social_signals_section(ts, styles: dict) -> list:
    S = styles
    items: list = []

    items.append(Spacer(1, 8))
    items.append(Paragraph("Social Signals", S["h1"]))
    items.append(_navy_rule())
    items.append(Paragraph(
        "Observed activity from public sources over the past 90 days.",
        S["body_italic"],
    ))
    items.append(Spacer(1, 4))

    signals = ts.social_signals
    if not signals:
        items.append(Paragraph("No public social signals identified.", S["body_italic"]))
        return items

    for sig in signals[:8]:  # cap display for layout headroom
        title    = _safe_str(getattr(sig, "title", None), "Untitled")
        summary  = _safe_str(getattr(sig, "summary", None), "")
        pub_date = _safe_str(getattr(sig, "published_date", None))
        sig_type = _safe_str(getattr(sig, "signal_type", None))
        source   = _safe_str(getattr(sig, "source", None))
        url      = _safe_str(getattr(sig, "url", None))
        score    = getattr(sig, "relevance_score", None)

        # Header row: title + metadata
        meta = " · ".join(filter(lambda x: x and x != "—",
                                 [pub_date, sig_type.replace("_", " ").title(),
                                  source.replace("_", " ")]))
        items.append(Paragraph(
            f'<b>{_truncate(title, 80)}</b>  '
            f'<font color="#767676" size="7">{meta}</font>',
            S["body"],
        ))
        if summary:
            items.append(Paragraph(
                f"Observed activity: {_truncate(summary, 300)}",
                S["signal_body"],
            ))
        if url and url != "—":
            items.append(Paragraph(url, S["url_sm"]))
        if score is not None:
            items.append(Paragraph(
                f"Relevance score: {score}/10",
                S["body_sm"],
            ))
        items.append(Spacer(1, 5))

    if len(signals) > 8:
        items.append(Paragraph(
            f"… and {len(signals) - 8} additional signal(s) not shown.",
            S["body_italic"],
        ))

    return items


