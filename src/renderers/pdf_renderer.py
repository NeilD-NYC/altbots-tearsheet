class _PageDecorator:
    """Callable passed to BaseDocTemplate as onPage / onLaterPages."""

    def __init__(self, manager_name: str, generated_date: str, styles: dict):
        self.manager_name  = manager_name
        self.generated_date = generated_date
        self.styles = styles

    def __call__(self, canvas: Canvas, doc: BaseDocTemplate) -> None:
        canvas.saveState()
        self._draw_header(canvas)
        self._draw_footer(canvas, doc.page)
        canvas.restoreState()

    def _draw_header(self, canvas: Canvas) -> None:
        """Full-width navy header bar with firm name and branding."""
        canvas.setFillColor(NAVY)
        canvas.rect(0, PAGE_H - HEADER_H, PAGE_W, HEADER_H, fill=1, stroke=0)

        # Gold accent rule at bottom of header
        canvas.setStrokeColor(GOLD)
        canvas.setLineWidth(2)
        canvas.line(0, PAGE_H - HEADER_H, PAGE_W, PAGE_H - HEADER_H)

        # Manager name (left)
        canvas.setFillColor(WHITE)
        canvas.setFont("Helvetica-Bold", 13)
        canvas.drawString(MARGIN_H, PAGE_H - HEADER_H + 14,
                          _truncate(self.manager_name, 60))

        # "Intelligence Report" label (right)
        canvas.setFont("Helvetica", 8)
        canvas.setFillColor(GOLD)
        canvas.drawRightString(PAGE_W - MARGIN_H, PAGE_H - HEADER_H + 14,
                               "Institutional Intelligence Report")

        # Sub-line: "Prepared by AltBots"
        canvas.setFont("Helvetica-Oblique", 7)
        canvas.setFillColor(colors.HexColor("#A0A8B8"))
        canvas.drawRightString(PAGE_W - MARGIN_H, PAGE_H - HEADER_H + 4,
                               "Prepared by AltBots")

    def _draw_footer(self, canvas: Canvas, page_num: int) -> None:
        """Mandatory footer: 'Generated by AltBots [Date]' + page number."""
        y = MARGIN_BOT - 16
        canvas.setFont("Helvetica", 7)
        canvas.setFillColor(GRAY_MID)
        canvas.drawCentredString(
            PAGE_W / 2, y,
            f"Generated by AltBots  {self.generated_date}",
        )
        canvas.drawRightString(PAGE_W - MARGIN_H, y,
                               f"Page {page_num}")


