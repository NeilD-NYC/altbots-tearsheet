def _build_firm_overview(ts, styles: dict) -> list:
    S = styles
    items: list = []

    items.append(Paragraph("Firm Overview", S["h1"]))
    items.append(_navy_rule())

    identity = ts.identity
    edgar    = ts.edgar

    # Key-value grid
    rows = []

    if identity:
        rows.append(("Manager", _safe_str(getattr(identity, "legal_name", None)
                                          or ts.manager_name)))
        if getattr(identity, "crd", None):
            rows.append(("CRD Number", identity.crd))
        if getattr(identity, "cik", None):
            rows.append(("CIK", identity.cik))
        if getattr(identity, "website", None):
            rows.append(("Website", identity.website))
        handles = getattr(identity, "social_handles", {})
        if handles:
            for platform, handle in list(handles.items())[:3]:
                rows.append((platform.title(), _safe_str(handle)))
    else:
        rows.append(("Manager", ts.manager_name))

    if edgar and edgar.adv:
        adv = edgar.adv
        rows.append(("SEC Registered",
                     "Yes" if getattr(adv, "sec_registered", False) else "No"))
        rows.append(("Registration Status",
                     _safe_str(getattr(adv, "registration_status", None))))
        rows.append(("AUM (Total)",
                     _fmt_usd(getattr(adv, "aum_total_usd", None))))
        rows.append(("AUM (Discretionary)",
                     _fmt_usd(getattr(adv, "aum_discretionary_usd", None))))
        if getattr(adv, "num_clients", None) is not None:
            rows.append(("Number of Clients", str(adv.num_clients)))
        if getattr(adv, "min_account_size", None):
            rows.append(("Min Account Size",
                         _safe_str(adv.min_account_size)))
        if getattr(adv, "client_types", None):
            rows.append(("Client Types",
                         _truncate(", ".join(str(c) for c in adv.client_types), 80)))
        if getattr(adv, "fee_structures", None):
            rows.append(("Fee Structures",
                         _truncate(", ".join(str(f) for f in adv.fee_structures), 80)))

    if not rows:
        items.append(Paragraph("No overview data available.", S["body_italic"]))
        return items

    col_w = [1.4 * inch, CONTENT_W - 1.4 * inch]
    tdata = [
        [Paragraph(k, S["kv_key"]), Paragraph(v, S["kv_val"])]
        for k, v in rows
    ]
    tbl = Table(tdata, colWidths=col_w, repeatRows=0)
    tbl.setStyle(TableStyle([
        ("VALIGN",       (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING",  (0, 0), (-1, -1), 2),
        ("RIGHTPADDING", (0, 0), (-1, -1), 2),
        ("TOPPADDING",   (0, 0), (-1, -1), 2),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 2),
        ("ROWBACKGROUNDS", (0, 0), (-1, -1), [WHITE, GRAY_LIGHT]),
        ("GRID",         (0, 0), (-1, -1), 0.25, colors.HexColor("#D0D0D0")),
    ]))
    items.append(tbl)

    # Source attribution
    if edgar and edgar.adv and hasattr(edgar.adv, "source") and edgar.adv.source:
        src = edgar.adv.source
        items.append(Paragraph(
            f"Source: Form ADV · Filed {_safe_str(getattr(src, 'filing_date', None))} · "
            f"Accession {_safe_str(getattr(src, 'accession_number', None))}",
            S["body_italic"],
        ))

    return items


def _build_personnel_table(ts, styles: dict) -> list:
    S = styles
    items: list = []

    items.append(Spacer(1, 8))
    items.append(Paragraph("Key Personnel", S["h1"]))
    items.append(_navy_rule())

    if not ts.personnel:
        items.append(Paragraph("No personnel data available.", S["body_italic"]))
        return items

    headers = ["Name", "Title", "Background"]
    col_w = [1.35 * inch, 1.35 * inch, CONTENT_W - 2.70 * inch]

    tdata: list = [[Paragraph(h, S["tbl_hdr"]) for h in headers]]

    for p in ts.personnel:
        name  = _safe_str(getattr(p, "name", None))
        title = _safe_str(getattr(p, "title", None))
        bio   = _truncate(_safe_str(getattr(p, "bio", None), "No bio available."), 220)
        tdata.append([
            Paragraph(name,  S["tbl_cell"]),
            Paragraph(title, S["tbl_cell"]),
            Paragraph(bio,   S["tbl_cell_sm"]),
        ])

    tbl = Table(tdata, colWidths=col_w, repeatRows=1)
    tbl.setStyle(TableStyle([
        ("BACKGROUND",   (0, 0), (-1, 0), NAVY),
        ("TEXTCOLOR",    (0, 0), (-1, 0), WHITE),
        ("VALIGN",       (0, 0), (-1, -1), "TOP"),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, GRAY_LIGHT]),
        ("LEFTPADDING",  (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("TOPPADDING",   (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 3),
        ("GRID",         (0, 0), (-1, -1), 0.25, colors.HexColor("#C0C0C0")),
    ]))
    items.append(tbl)
    return items


def _build_sec_filings_snapshot(ts, styles: dict) -> list:
    S = styles
    items: list = []

    items.append(Spacer(1, 8))
    items.append(Paragraph("SEC Filings Snapshot", S["h1"]))
    items.append(_navy_rule())

    edgar = ts.edgar
    if not edgar:
        items.append(Paragraph("No SEC filing data available.", S["body_italic"]))
        return items

    # ── 13F holdings ──────────────────────────────────────────────────────
    if edgar.form_13f:
        f13 = edgar.form_13f[0]  # most recent
        items.append(Paragraph("Form 13F — Portfolio Holdings", S["h2"]))
        meta_rows = [
            ("Portfolio Value",
             _fmt_usd(getattr(f13, "total_portfolio_value_usd", None))),
            ("Positions",
             _safe_str(getattr(f13, "num_positions", None))),
            ("Top-10 Concentration",
             f"{f13.concentration_top10_pct:.1f}%"
             if getattr(f13, "concentration_top10_pct", None) is not None
             else "—"),
        ]
        if hasattr(f13, "recency") and f13.recency:
            flag  = getattr(f13.recency, "flag",  "red")
            days  = getattr(f13.recency, "days_since_filing", -1)
            meta_rows.insert(0, ("Recency", _recency_label(flag, days)))

        col_w = [1.4 * inch, CONTENT_W - 1.4 * inch]
        tdata = [[Paragraph(k, S["kv_key"]), Paragraph(v, S["kv_val"])]
                 for k, v in meta_rows]
        tbl = Table(tdata, colWidths=col_w)
        tbl.setStyle(TableStyle([
            ("ROWBACKGROUNDS", (0, 0), (-1, -1), [WHITE, GRAY_LIGHT]),
            ("TOPPADDING",    (0, 0), (-1, -1), 2),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 2),
            ("LEFTPADDING",   (0, 0), (-1, -1), 2),
            ("GRID",          (0, 0), (-1, -1), 0.25,
             colors.HexColor("#D0D0D0")),
        ]))
        items.append(tbl)

        # Top holdings mini-table
        holdings = getattr(f13, "top_holdings", [])[:8]
        if holdings:
            items.append(Spacer(1, 4))
            items.append(Paragraph("Top Holdings", S["h3"]))
            h_headers = ["Security", "Value", "% of Portfolio"]
            h_col_w = [CONTENT_W * 0.52, CONTENT_W * 0.25, CONTENT_W * 0.23]
            h_data: list = [[Paragraph(h, S["tbl_hdr"]) for h in h_headers]]
            for h in holdings:
                pct = (f"{h.pct_of_portfolio:.2f}%"
                       if getattr(h, "pct_of_portfolio", None) is not None
                       else "—")
                h_data.append([
                    Paragraph(_truncate(_safe_str(getattr(h, "name", None)), 50),
                               S["tbl_cell_sm"]),
                    Paragraph(_fmt_usd(getattr(h, "value_usd", None)),
                               S["tbl_cell_sm"]),
                    Paragraph(pct, S["tbl_cell_sm"]),
                ])
            h_tbl = Table(h_data, colWidths=h_col_w, repeatRows=1)
            h_tbl.setStyle(TableStyle([
                ("BACKGROUND",     (0, 0), (-1, 0), NAVY),
                ("TEXTCOLOR",      (0, 0), (-1, 0), WHITE),
                ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, GRAY_LIGHT]),
                ("TOPPADDING",     (0, 0), (-1, -1), 2),
                ("BOTTOMPADDING",  (0, 0), (-1, -1), 2),
                ("LEFTPADDING",    (0, 0), (-1, -1), 4),
                ("GRID",           (0, 0), (-1, -1), 0.25,
                 colors.HexColor("#C8C8C8")),
            ]))
            items.append(h_tbl)

        if hasattr(f13, "source") and f13.source:
            src = f13.source
            items.append(Paragraph(
                f"Source: {_safe_str(getattr(src, 'form_type', 'Form 13F'))} · "
                f"Filed {_safe_str(getattr(src, 'filing_date', None))} · "
                f"Accession {_safe_str(getattr(src, 'accession_number', None))}",
                S["body_italic"],
            ))

    # ── Form D raises ─────────────────────────────────────────────────────
    if edgar.form_d:
        items.append(Spacer(1, 6))
        items.append(Paragraph("Form D — Private Placement Offerings", S["h2"]))
        d_headers = ["Fund", "Type", "Amount Raised", "First Sale"]
        d_col_w = [CONTENT_W * 0.34, CONTENT_W * 0.22,
                   CONTENT_W * 0.22, CONTENT_W * 0.22]
        d_data: list = [[Paragraph(h, S["tbl_hdr"]) for h in d_headers]]
        for fd in edgar.form_d[:5]:
            d_data.append([
                Paragraph(_truncate(_safe_str(getattr(fd, "fund_name", None)), 40),
                           S["tbl_cell_sm"]),
                Paragraph(_safe_str(getattr(fd, "offering_type", None)),
                           S["tbl_cell_sm"]),
                Paragraph(_fmt_usd(getattr(fd, "amount_raised_usd", None)),
                           S["tbl_cell_sm"]),
                Paragraph(_safe_str(getattr(fd, "date_of_first_sale", None)),
                           S["tbl_cell_sm"]),
            ])
        d_tbl = Table(d_data, colWidths=d_col_w, repeatRows=1)
        d_tbl.setStyle(TableStyle([
            ("BACKGROUND",     (0, 0), (-1, 0), NAVY),
            ("TEXTCOLOR",      (0, 0), (-1, 0), WHITE),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, GRAY_LIGHT]),
            ("TOPPADDING",     (0, 0), (-1, -1), 2),
            ("BOTTOMPADDING",  (0, 0), (-1, -1), 2),
            ("LEFTPADDING",    (0, 0), (-1, -1), 4),
            ("GRID",           (0, 0), (-1, -1), 0.25,
             colors.HexColor("#C8C8C8")),
        ]))
        items.append(d_tbl)

    if not edgar.form_13f and not edgar.form_d and not edgar.adv:
        items.append(Paragraph("No SEC filing records retrieved.", S["body_italic"]))

    return items


